//定义一个找素数的函数
int is_prime(int num)
{
	int i = 2;
	for (i = 2; i <= (sqrt(num)); i++)
	{
		if ((num % i) == 0)
		{
			return 0;
		}

	}
	if (i > (sqrt(num)))
	{
		return 1;
	}
}

int main()
{
	int i = 100;
	int j = 0;
	//求出100 - 200之间的素数
	for (i = 100; i <= 200; i++)
	{
		if (is_prime(i) == 1)
		{
			printf("%d是素数\n", i);
			j++;
		}
		else
		{
			continue;
		}

	}
	printf("一共有%d个素数\n", j);
}
